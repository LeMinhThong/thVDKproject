/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  	  //start sonic sensor + oled
  	  HAL_TIM_Base_Start(&htim1);
  	  HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  // pull the TRIG pin low
  	  SSD1306_Init();

  	  //start ADC
  	  HAL_ADC_Start(&hadc1);

  	  //start motor B
  	  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, 0);
  	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 0);

  	  //start motor A
  	  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);
  	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, 0);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, 0);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);  // pull the TRIG pin HIGH
	  __HAL_TIM_SET_COUNTER(&htim1, 0);
	  while (__HAL_TIM_GET_COUNTER (&htim1) < 10);  // wait for 10 us
	  HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  // pull the TRIG pin low

	  //HAL_GetTick: return counter of system timer
	  pMillis = HAL_GetTick(); // used this to avoid infinite while loop  (for timeout)
	  // wait for the echo pin to go high
	  while (!(HAL_GPIO_ReadPin (ECHO_PORT, ECHO_PIN)) && pMillis + 10 >  HAL_GetTick());
	  Value1 = __HAL_TIM_GET_COUNTER (&htim1);

	  pMillis = HAL_GetTick(); // used this to avoid infinite while loop (for timeout)
	  // wait for the echo pin to go low
	  while ((HAL_GPIO_ReadPin (ECHO_PORT, ECHO_PIN)) && pMillis + 50 > HAL_GetTick());
	  Value2 = __HAL_TIM_GET_COUNTER (&htim1);

	  Distance = (Value2-Value1)* 0.034/2;
	  //(Value2-Value1) arrival and return time (us)
	  //((Value2-Value1)(us) / 1000000)(s)
	  //0.034 cm/us speed of sonic

	  //analogRead
	  HAL_ADC_PollForConversion(&hadc1,1000);
	  readValue = HAL_ADC_GetValue(&hadc1);

	  //speed(300:1000)(PWM)(compare/autoReload) <=> readValue(2900:3100) (analogRead)
	  speed = readValue * 7/2.0 - 9850;
	  if(speed > 1000)	speed = 1000;
	  if(speed < 300)		speed = 0;

	  SSD1306_GotoXY (0, 0);
	  if(Distance > 40)
	  {
		  //control oled
		  SSD1306_Puts ("Normal  ", &Font_11x18, 1);
		  //control buzzer
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, 0); 	//disable buzzer

		  //speed 100% depend on potentiometer

	  }	else if(Distance <=40 && Distance >20)
	  {
		  warn = 20 * Distance;	//delay period of warning mode
		  //control oled
		  SSD1306_Puts ("Warning!", &Font_11x18, 1);

		  //control buzzer
		  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_8);
		  HAL_Delay(warn);

		  if(speed > 510)	speed = 510; //lock speed 510 <=> speedDisplay 30%
	  } else if (Distance <= 20)
	  {
		  warn = Distance * 10; //delay period of stop mode (f stopMode > f warningMode)
		  //control oled
		  SSD1306_Puts ("Stop!!!!", &Font_11x18, 1);

		  //control buzzer
		  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_8);
		  HAL_Delay(warn);

		  speed = 0;	//stop
	  }

	  //control motor B
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, 0);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 1);
	  __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_2, speed); //PWM speed / autoReload => channel 2

	  //control motor A
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, 1);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, 0);
	  __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_4, speed); //PWM speed / autoReload => channel 4


	  //note:
	  //readValue(2900:3100) <=> speed(300:1000) <=> speedDisplay(0:100)
	  //  mV						compare				%
	  // readValue < 2900 => speed = 0 => speedDisplay = 0
	  // lim(readValue -> 2900+) => speed -> 300+ => speedDisplay => 0+%
	  // lim(readValue -> 3100-) => speed -> 1000- => speedDisplay => 100-%
	  // readValue > 3100 => speed = 1000 => speedDisplay = 100%

	  //print line 2 of oled
	  sprintf(strDistance,"%d    ", Distance); //char <- float
	  SSD1306_GotoXY (0, 18);
	  SSD1306_Puts (strDistance, &Font_11x18, 1);
	  SSD1306_GotoXY (35, 18);
	  SSD1306_Puts ("(cm)", &Font_11x18, 1);

	  //print line 3
	  SSD1306_GotoXY (0, 40);
	  SSD1306_Puts ("Speed:", &Font_11x18, 1);

	  speedDisplay = (int)speed;
	  speedDisplay = (int)((speedDisplay-300)/7);	//speed(300:1000) <=> speedDisplay(0:100)
	  if(speed<= 300)	speedDisplay = 0;
	  sprintf(strSpeed,"%d  ", speedDisplay); 	//char <- int
	  SSD1306_GotoXY (66, 40);
	  SSD1306_Puts (strSpeed, &Font_11x18, 1);

	  SSD1306_GotoXY (100, 40);
	  SSD1306_Puts ("%", &Font_11x18, 1);

	  SSD1306_UpdateScreen();
	  HAL_Delay(50);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

